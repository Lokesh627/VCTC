package datatypes;

public class Primitive_Datatypes {

	public static void main(String[] args) {
		
		//Primitive Datatypes-->Keywords-->MemorySize-->Fix//
		//Number/Non-Decimal-->byte,short,int,long//
		
		byte rollno=16;
		short rank= 1700;
		int population=8090920;
		long money=128021029l;
		
		System.out.println("Roll number is "+rollno);
		System.out.println("Rank is "+rank);
		System.out.println("Population is "+population);
		System.out.println("Money is "+money);
		
		
		//Decimal Number-->float, double//
		float percentagemarks=87.50f;
		double GDP=5.5d;
		
		System.out.println("My Percentage is "+percentagemarks);
		System.out.println("GDP of country is "+GDP);
		
		//For Character we use-->char//
		char grade='A';
		
		System.out.println("My grade is "+grade);
		
		//For true and false we use-->boolean//
		boolean result=true;
		
		System.out.println("My result is "+result);
	}

}
